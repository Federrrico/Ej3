    private void jCBCategoriasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jCBCategoriasActionPerformed
        String categoriaString = jCBCategorias.getSelectedItem().toString();
        modelo.setRowCount(0);
        
        for (Producto producto : ListadoProductos) {
            if (producto.getCategoriaP().equalsIgnoreCase(categoriaString)) {
                    cargarTabla(producto);
                }
        }
    }//GEN-LAST:event_jCBCategoriasActionPerformed

    private void jBAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAgregarActionPerformed
        String categoriaString = jCBCategorias.getSelectedItem().toString();
                
        String nombreP = JOptionPane.showInputDialog(this, "INGRESE EL NOMBRE DEL PRODUCTO");
        Double precioP = Double.parseDouble(JOptionPane.showInputDialog(this, "INGRESE EL NOMBRE DEL PRODUCTO"));
        
        Producto prod = new Producto(categoriaString, nombreP, precioP);
        
        ListadoProductos.add(prod);
        
        cargarTabla(prod);
        
        modelo.fireTableDataChanged();
    }//GEN-LAST:event_jBAgregarActionPerformed
    
    
    
