private void armarCabecera(){
    modelo.addColumn("CATEGORIA");
    modelo.addColumn("PRODUCTO");
    modelo.addColumn("PRECIO");
    jTProductos.setModel(modelo);
}

private void cargarTabla(Producto producto){
    modelo.addRow(new Object[]{producto.getCategoriaP(),producto.getNombreP(),producto.getPrecioP()});
    jTFResultadoP.setText("PRODUCTO AGREGADO!");
}




